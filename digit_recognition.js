var neuralnet = undefined;
var trainer;
var image_dimension = 28;

$.getScript('https://eesh.github.io/scratch-test/pica.js', checkPicaLoaded);
$.getScript('https://eesh.github.io/scratch-test/convnet.js', initNeuralNet);

function checkPicaLoaded() {
  if(window.pica == undefined) {
    console.log("pica not loaded");
  }
}

function initNeuralNet() {
  if(neuralnet == undefined) {
    var json = getJson();
    console.log(json);
    neuralnet = new convnetjs.Net();
    neuralnet.fromJSON(json);
    trainer = new convnetjs.SGDTrainer(neuralnet, {learning_rate: 0.0001, momentum: 0.9, batch_size: 2, l2_decay: 0.00001});
    console.log("digit_recognition.js loaded");
  }
}

function getDigit(imageData, callback) {
  
  function testImage(img) {
    imageElement = new Image();
    imageElement.onload = function() {
      var x = convnetjs.img_to_vol(imageElement);
      console.log(x);
      var out_p = neuralnet.forward(x);

      var preds =[];
      for(var k=0;k<out_p.w.length;k++) {
        preds.push({k:k,p:out_p.w[k]});
      }
      preds.sort(function(a,b){return a.p<b.p ? 1:-1;});
      console.log(preds);
      callback(preds[0].k);
    }
    imageElement.src = img;
  }

  function doResize(data) {
    resize(data, testImage);
  }

  centerCrop(imageData, doResize);
}



function centerCrop(src, callback){
    var image = new Image();

    image.onload = function() {
      var max_width = Math.min(image.width, image.height);
      var max_height = Math.min(image.width, image.height);

      var canvas = document.createElement('canvas');
      var ctx = canvas.getContext("2d");

      ctx.clearRect(0, 0, canvas.width, canvas.height);
      canvas.width = max_width;
      canvas.height = max_height;
      ctx.drawImage(image, (max_width - image.width)/2, (max_height - image.height)/2, image.width, image.height);
      callback(canvas.toDataURL("image/png"));
    };
    image.src = src;
}

function resize(src, callback){
    var image = new Image();

    image.onload = function() {
      var canvas = document.createElement('canvas');
      canvas.width = image.width;
      canvas.height = image.height;
      var ctx = canvas.getContext("2d");
      ctx.clearRect(0, 0, canvas.width, canvas.height);
      ctx.drawImage(image, 0, 0, image.width, image.height);

      var dst = document.createElement('canvas');
      dst.width = image_dimension;
      dst.height = image_dimension;

      window.pica.WW = false;
      window.pica.resizeCanvas(canvas, dst, {
        quality: 2,
        unsharpAmount: 500,
        unsharpThreshold: 100,
        transferable: false
      }, function (err) {  });
      window.pica.WW = true;
      callback(dst.toDataURL("image/png"));
    }

    image.src = src;
}


